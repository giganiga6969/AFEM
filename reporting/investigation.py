"""
AFEM Investigation Reporting — Phase 5
========================================
Aggregates IntegrityReport (embedded in TimelineReport) + TimelineReport +
AttributionReport into one InvestigationReport, and renders it to JSON and
self-contained HTML.

Responsibility separation
--------------------------
build_investigation_report(...)   -> InvestigationReport   (pure aggregation)
render_investigation_html(...)    -> str                    (pure rendering)
write_investigation_json(...)     -> Path                    (pure persistence)
write_investigation_html(...)     -> Path                    (pure persistence)
generate_investigation_report(...) -> orchestrates the above four, nothing more

Non-negotiable invariants
--------------------------
* attribution_class, confidence_score, confidence_label, is_reliable,
  task_outcome, evidence_trust, reconstruction_completeness,
  authorization, observed_actions, unauthorized_actions,
  injection_assessment, injection_indicators, triggered_rules,
  confidence_contributions, uncertainty_reasons, supporting_evidence,
  contradictory_evidence are copied VERBATIM from AttributionReport.
  This module never recomputes, reinterprets, or overrides any of them.

* If an internal consistency check fails (e.g. evidence_trust says
  compromised but is_reliable is somehow True), the upstream value is
  preserved unchanged and a note is appended to reporting_warnings.
  Nothing here silently "fixes" upstream data.

* report_id, case_title, case_summary, investigator_findings,
  investigator_conclusion, limitations, and reporting_warnings are all
  deterministic - selected by fixed templates keyed on already-known
  upstream state, never generated by an LLM and never randomised.
  generated_at is the only field permitted to vary between runs.

Changes vs. the prior draft of this module
-------------------------------------------
* ``_atomic_write_text`` now cleans up its temp file on any failure
  (write, fsync, or replace) instead of leaving an orphaned ``*.tmpNNNN``
  file next to the destination. The temp filename also includes a
  short uuid suffix (not just the PID) so two writes to the same
  destination within one process cannot collide.
* HTML rendering of list-valued fields (authorized/prohibited/observed/
  unauthorized tools, tool sequence) now renders as an escaped
  comma-joined string instead of a Python list repr (`"['a', 'b']"`).
  This was a readability defect, not a security defect: the previous
  code always passed the value through ``str()`` before ``html.escape``,
  so no unescaped content could reach the page.
* Triggered-rule and confidence-contribution rows now also surface
  ``TriggeredRule.evidence_refs`` and ``RuleContribution.description``
  (previously silently dropped) so investigators are not missing
  fields that the Phase 4 schema actually populates.
* ``report_version`` is now passed explicitly rather than relying on
  the schema default, per the review requirement to confirm it is
  populated deterministically.
"""
