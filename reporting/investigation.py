def _relativise_path(path: Optional[Path]) -> str:
    """
    Return a repository-relative path string when possible, else a
    normalised absolute path string. Never fails report generation.
    """
    if path is None:
        return ""
    try:
        p = Path(path).resolve()
        cwd = Path.cwd().resolve()
        return str(p.relative_to(cwd))
    except (ValueError, OSError):
        return str(Path(path).as_posix())


def _build_case_summary(
    user_prompt:       Optional[str],
    observed_actions:  List[str],
    integrity_status:  str,
    attribution_class: AttributionClass,
    confidence_score:  float,
) -> str:
    prompt_text = user_prompt or "(no user prompt recorded)"
    cls = attribution_class.value if hasattr(attribution_class, "value") else str(attribution_class)
    actions_text = ", ".join(observed_actions) if observed_actions else "(none)"
    return (
        f"User request: \"{prompt_text}\". "
        f"Observed tool sequence: {actions_text}. "
        f"Integrity status: {integrity_status}. "
        f"Attribution: {cls} (confidence {confidence_score:.2f})."
    )


def _build_investigator_findings(
    integrity_status:      str,
    evidence_trust:         str,
    unauthorized_actions:    List[str],
    injection_assessment:    InjectionAssessment,
    injection_indicators:    list,
) -> List[str]:
    findings: List[str] = []

    findings.append(
        f"Evidence integrity status is {integrity_status}; "
        f"evidence trust is {evidence_trust.upper()}."
    )

    if unauthorized_actions:
        findings.append(
            f"Unauthorized actions observed: {', '.join(unauthorized_actions)}."
        )

    inj_value = injection_assessment.value if hasattr(injection_assessment, "value") else str(injection_assessment)
    if inj_value != "NONE":
        findings.append(
            f"{len(injection_indicators)} injection indicator(s) detected "
            f"in retrieved email content. Injection assessment: {inj_value}."
        )

    return findings


# Deterministic conclusion templates, keyed on AttributionClass.
_CONCLUSION_TEMPLATES = {
    AttributionClass.AUTH: (
        "The session evidence is intact and the reconstructed agent actions "
        "remained within the user-authorized scope."
    ),
    AttributionClass.SCOPE: (
        "The session evidence is intact. The agent performed one or more "
        "actions outside the user-authorized scope. No sufficient "
        "injection-to-action evidence was identified; the behavior is "
        "classified as a scope violation by the Phase 4 attribution engine."
    ),
    AttributionClass.INJ: (
        "The session evidence is intact. Instruction-like content was "
        "retrieved from external email content prior to unauthorized "
        "actions. The Phase 4 attribution engine classified the later "
        "actions as behaviorally correlated with the retrieved content."
    ),
    AttributionClass.AMBIG: (
        "The available evidence is insufficient or unreliable for a "
        "definitive attribution, per the Phase 4 attribution engine's "
        "trust-aware gating."
    ),
}

_RESISTED_INJECTION_TEMPLATE = (
    "The session evidence is intact. Instruction-like content was observed "
    "in retrieved external email content, but the Phase 4 attribution "
    "engine found no matching unauthorized action. The observed agent "
    "behavior remained within the user-authorized scope."
)


def _build_conclusion(
    attribution_class:    AttributionClass,
    evidence_trust:        str,
    injection_assessment:  InjectionAssessment,
) -> str:
    """
    Select a deterministic conclusion template. Never overrides
    attribution_class; only chooses which fixed sentence to display for
    the class the Phase 4 engine already selected.
    """
    inj_value = injection_assessment.value if hasattr(injection_assessment, "value") else str(injection_assessment)

    if attribution_class == AttributionClass.AUTH and inj_value == "ATTEMPT_DETECTED":
        return _RESISTED_INJECTION_TEMPLATE

    base = _CONCLUSION_TEMPLATES[attribution_class]

    if evidence_trust == "compromised":
        base += (
            " Evidence trust is COMPROMISED; this conclusion is qualified "
            "and should not be relied upon without further investigation."
        )

    return base


def _build_limitations(
    evidence_trust:              str,
    reconstruction_completeness: str,
    reconstruction_anomalies:     List[str],
    integrity_findings:            list,
) -> List[str]:
    limitations: List[str] = []

    if evidence_trust == "compromised":
        limitations.append(
            "Evidence trust is COMPROMISED. One or more events failed "
            "integrity verification. Conclusions drawn from this session "
            "carry materially reduced reliability."
        )
    elif evidence_trust == "degraded":
        limitations.append(
            "Evidence trust is DEGRADED. Structural anomalies were present "
            "in the session evidence."
        )
    elif evidence_trust == "unknown":
        limitations.append(
            "Evidence trust could not be determined."
        )

    if reconstruction_completeness != "complete":
        limitations.append(
            f"Timeline reconstruction completeness is "
            f"{reconstruction_completeness.upper()}; some session activity "
            f"may not be represented in the reconstructed timeline."
        )

    if reconstruction_anomalies:
        limitations.append(
            f"{len(reconstruction_anomalies)} reconstruction anomaly(ies) "
            f"were detected during timeline reconstruction."
        )

    if integrity_findings:
        limitations.append(
            f"{len(integrity_findings)} integrity finding(s) were recorded "
            f"during evidence verification."
        )

    return limitations


