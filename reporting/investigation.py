def _escape(value: object) -> str:
    """Centralised escaping helper. Every dynamic HTML value passes through this."""
    return html.escape(str(value), quote=True)


def _escape_list(items: List[str], empty: str = "(none)") -> str:
    """Render a list of strings as an escaped, comma-joined string."""
    if not items:
        return html.escape(empty, quote=True)
    return html.escape(", ".join(str(i) for i in items), quote=True)


def render_investigation_html(report: InvestigationReport) -> str:
    """
    Pure rendering: produce a self-contained, static, dependency-free HTML
    string from an already-built InvestigationReport. Performs no analysis
    and no file I/O.
    """
    e = _escape

    def _rows(items: List[str], empty: str = "(none)") -> str:
        if not items:
            return f"<p class='muted'>{e(empty)}</p>"
        return "<ul>" + "".join(f"<li>{e(i)}</li>" for i in items) + "</ul>"

    def _timeline_rows() -> str:
        if not report.timeline:
            return "<p class='muted'>(no timeline entries)</p>"
        rows = []
        for entry in report.timeline:
            refs = "; ".join(
                f"{e(r.get('artifact_type','?'))}:{e(r.get('artifact_id','?'))}"
                for r in (entry.artifact_refs or [])
            ) or "(none)"
            rows.append(
                "<tr>"
                f"<td>{e(entry.sequence_number)}</td>"
                f"<td>{e(entry.timestamp or '')}</td>"
                f"<td>{e(entry.actor)}</td>"
                f"<td>{e(entry.event_type)}</td>"
                f"<td>{e(entry.tool_name or '')}</td>"
                f"<td>{e(entry.action)}</td>"
                f"<td>{e(entry.input_summary or '')}</td>"
                f"<td>{e(entry.output_summary or '')}</td>"
                f"<td>{refs}</td>"
                f"<td>{e(entry.integrity_status)}</td>"
                f"<td>{e(entry.anomaly or '')}</td>"
                "</tr>"
            )
        return (
            "<table><thead><tr>"
            "<th>Seq</th><th>Timestamp</th><th>Actor</th><th>Type</th>"
            "<th>Tool</th><th>Action</th><th>Input</th><th>Output</th>"
            "<th>Artifacts</th><th>Integrity</th><th>Anomaly</th>"
            "</tr></thead><tbody>" + "".join(rows) + "</tbody></table>"
        )

    def _findings_rows() -> str:
        if not report.integrity_findings:
            return "<p class='muted'>(no integrity findings)</p>"
        rows = []
        for f in report.integrity_findings:
            ftype = getattr(f.finding_type, "value", f.finding_type) if f.finding_type else ""
            sev   = getattr(f.severity, "value", f.severity) if f.severity else ""
            rows.append(
                f"<li><strong>{e(sev).upper()}</strong> - {e(ftype)} "
                f"(seq {e(f.affected_sequence)}): {e(f.message)}</li>"
            )
        return "<ul>" + "".join(rows) + "</ul>"

    def _rules_rows() -> str:
        if not report.triggered_rules:
            return "<p class='muted'>(no rules triggered)</p>"
        rows = []
        for r in report.triggered_rules:
            refs_text = ""
            if r.evidence_refs:
                refs_text = " <span class='muted'>[refs: seq " + ", ".join(
                    e(ref.timeline_sequence) for ref in r.evidence_refs
                ) + "]</span>"
            rows.append(
                f"<li><strong>{e(r.rule_id)}</strong>: {e(r.description)} "
                f"&rarr; {e(r.outcome)}{refs_text}</li>"
            )
        return "<ul>" + "".join(rows) + "</ul>"

    def _confidence_rows() -> str:
        if not report.confidence_contributions:
            return "<p class='muted'>(no confidence contributions)</p>"
        rows = []
        for c in report.confidence_contributions:
            sign = "+" if c.delta >= 0 else ""
            rows.append(
                f"<li><strong>{e(c.rule_id)}</strong> {sign}{e(f'{c.delta:.2f}')} "
                f"- {e(c.description)}: {e(c.reason)}</li>"
            )
        return "<ul>" + "".join(rows) + "</ul>"

    def _injection_rows() -> str:
        if not report.injection_indicators:
            return "<p class='muted'>(no injection indicators detected)</p>"
        rows = []
        for ind in report.injection_indicators:
            rows.append(
                f"<li><strong>{e(ind.category)}</strong> matched "
                f"'{e(ind.phrase)}' (tool: {e(ind.tool_name)}, "
                f"seq: {e(ind.timeline_sequence)})"
                + (f"<br><span class='muted'>excerpt: {e(ind.content_excerpt)}</span>" if ind.content_excerpt else "")
                + "</li>"
            )
        return "<ul>" + "".join(rows) + "</ul>"

    def _evidence_refs(refs: list) -> str:
        if not refs:
            return "<p class='muted'>(none)</p>"
        rows = []
        for r in refs:
            rows.append(
                f"<li>seq {e(r.timeline_sequence)} - {e(r.event_type)} "
                f"({e(r.tool_name or '')}) - {e(r.reason)} "
                f"[integrity: {e(r.integrity_status)}]</li>"
            )
        return "<ul>" + "".join(rows) + "</ul>"

    trust_class = {
        "trusted": "ok", "degraded": "warn", "compromised": "bad", "unknown": "bad",
    }.get(report.evidence_trust, "warn")

    attribution_class_val = getattr(report.attribution_class, 'value', report.attribution_class)
    confidence_label_val = getattr(report.confidence_label, 'value', report.confidence_label)
    task_outcome_val = getattr(report.task_outcome, 'value', report.task_outcome)
    injection_assessment_val = getattr(report.injection_assessment, 'value', report.injection_assessment)

    html_doc = f"""<!DOCTYPE html>
<html lang="en">
<head>
<meta charset="UTF-8">
<title>{e(report.case_title)}</title>
<style>
  body {{ font-family: -apple-system, Segoe UI, Arial, sans-serif; margin: 2em; color: #1a1a1a; background: #fff; }}
  h1 {{ font-size: 1.6em; border-bottom: 2px solid #333; padding-bottom: 0.3em; }}
  h2 {{ font-size: 1.2em; margin-top: 1.6em; border-bottom: 1px solid #ccc; padding-bottom: 0.2em; }}
  table {{ border-collapse: collapse; width: 100%; font-size: 0.85em; margin-top: 0.5em; }}
  th, td {{ border: 1px solid #ccc; padding: 4px 8px; text-align: left; vertical-align: top; }}
  th {{ background: #f0f0f0; }}
  .muted {{ color: #777; font-style: italic; }}
  .badge {{ display: inline-block; padding: 2px 10px; border-radius: 4px; font-weight: bold; color: #fff; }}
  .ok {{ background: #2e7d32; }}
  .warn {{ background: #b8860b; }}
  .bad {{ background: #b00020; }}
  .grid {{ display: grid; grid-template-columns: 220px 1fr; gap: 4px 12px; max-width: 700px; }}
  .grid div:nth-child(odd) {{ font-weight: bold; }}
  ul {{ margin: 0.3em 0; padding-left: 1.4em; }}
  .conclusion {{ padding: 1em; border-left: 4px solid #333; background: #f7f7f7; }}
  @media print {{ body {{ margin: 0.5em; }} }}
</style>
</head>
<body>
<h1>{e(report.case_title)}</h1>
<p><strong>Report ID:</strong> {e(report.report_id)} &nbsp; <strong>Generated:</strong> {e(report.generated_at)} &nbsp; <strong>Schema:</strong> v{e(report.report_version)}</p>
<p>{e(report.case_summary)}</p>

<h2>Evidence Integrity</h2>
<div class="grid">
  <div>Integrity Status</div><div>{e(report.integrity_status)}</div>
  <div>Evidence Trust</div><div><span class="badge {trust_class}">{e(report.evidence_trust.upper())}</span></div>
  <div>Session Complete</div><div>{e(report.session_complete)}</div>
  <div>Genesis Hash</div><div><code>{e(report.genesis_hash)}</code></div>
  <div>Terminal Hash</div><div><code>{e(report.terminal_hash)}</code></div>
  <div>Verified At</div><div>{e(report.verified_at)}</div>
</div>
<h3>Integrity Findings</h3>
{_findings_rows()}

<h2>Reconstruction</h2>
<div class="grid">
  <div>Completeness</div><div>{e(report.reconstruction_completeness.upper())}</div>
  <div>User Prompt</div><div>{e(report.user_prompt or '(none)')}</div>
  <div>Source Session</div><div><code>{e(report.source_session_path)}</code></div>
</div>
<h3>Reconstruction Anomalies</h3>
{_rows(report.reconstruction_anomalies)}
<h3>Timeline</h3>
{_timeline_rows()}

<h2>Attribution</h2>
<div class="grid">
  <div>Attribution Class</div><div><strong>{e(attribution_class_val)}</strong></div>
  <div>Confidence Score</div><div>{e(f'{report.confidence_score:.2f}')}</div>
  <div>Confidence Label</div><div>{e(confidence_label_val)}</div>
  <div>Reliable</div><div><span class="badge {'ok' if report.reliable else 'bad'}">{e('Yes' if report.reliable else 'No')}</span></div>
  <div>Task Outcome</div><div>{e(task_outcome_val)}</div>
</div>

<h3>Authorization</h3>
<div class="grid">
  <div>Authorized Tools</div><div>{_escape_list(report.authorization.authorized_tools)}</div>
  <div>Prohibited Tools</div><div>{_escape_list(report.authorization.prohibited_tools)}</div>
  <div>Observed Actions</div><div>{_escape_list(report.observed_actions)}</div>
  <div>Unauthorized Actions</div><div>{_escape_list(report.unauthorized_actions)}</div>
</div>
{_rows(report.authorization.notes, empty="(no authorization notes)")}

<h3>Injection Assessment: {e(injection_assessment_val)}</h3>
{_injection_rows()}

<h3>Triggered Rules</h3>
{_rules_rows()}

<h3>Confidence Contributions</h3>
{_confidence_rows()}

<h3>Supporting Evidence</h3>
{_evidence_refs(report.supporting_evidence)}

<h3>Contradictory Evidence</h3>
{_evidence_refs(report.contradictory_evidence)}

<h3>Uncertainty Reasons</h3>
{_rows(report.uncertainty_reasons)}

<h2>Investigator Findings</h2>
{_rows(report.investigator_findings)}

<h2>Conclusion</h2>
<div class="conclusion">{e(report.investigator_conclusion)}</div>

<h2>Limitations</h2>
{_rows(report.limitations)}

<h2>Reporting Warnings</h2>
{_rows(report.reporting_warnings, empty="(no warnings)")}

</body>
</html>
"""
    return html_doc


# ---------------------------------------------------------------------------
# Safe persistence (atomic write via temp file + Path.replace)
# ---------------------------------------------------------------------------


def _atomic_write_text(path: Path, content: str) -> None:
    """
    Write text to path atomically: write to a temp sibling, flush, close,
    then Path.replace(). Works on Windows and POSIX; no third-party
    dependency required.

    On any failure (write, fsync, or replace) the temp file is removed
    before the exception propagates, so a failed report generation never
    leaves an orphaned ``*.tmp*`` file next to the destination.
    """
    path.parent.mkdir(parents=True, exist_ok=True)
    tmp_path = path.with_suffix(
        path.suffix + f".tmp{os.getpid()}{uuid.uuid4().hex[:8]}"
    )
    try:
        with open(tmp_path, "w", encoding="utf-8") as fh:
            fh.write(content)
            fh.flush()
            os.fsync(fh.fileno())
        tmp_path.replace(path)
    except BaseException:
        if tmp_path.exists():
            try:
                tmp_path.unlink()
            except OSError:
                pass
        raise


def write_investigation_json(report: InvestigationReport, output_dir: Path) -> Path:
    """
    Persist the authoritative JSON investigation report.

    Uses ``report.model_dump(mode="json")`` so every enum serializes to its
    plain string value, then ``json.dump`` with deterministic formatting.
    """
    output_dir.mkdir(parents=True, exist_ok=True)
    path = output_dir / f"{report.session_id}_investigation.json"
    data = report.model_dump(mode="json")
    content = json.dumps(data, ensure_ascii=False, indent=2)
    _atomic_write_text(path, content)
    return path


def write_investigation_html(report: InvestigationReport, output_dir: Path) -> Path:
    """Render and persist the self-contained HTML investigation report."""
    output_dir.mkdir(parents=True, exist_ok=True)
    path = output_dir / f"{report.session_id}_investigation.html"
    content = render_investigation_html(report)
    _atomic_write_text(path, content)
    return path


# ---------------------------------------------------------------------------
# Convenience orchestration (delegates only - no logic of its own)
# ---------------------------------------------------------------------------


def generate_investigation_report(
    timeline_report:     TimelineReport,
    attribution_report:  AttributionReport,
    output_dir:           Path,
    source_session_path:  Optional[Path] = None,
    write_html:            bool = True,
):
    """
    Convenience wrapper delegating to build_investigation_report(),
    write_investigation_json(), and optionally write_investigation_html().
    Contains no aggregation or rendering logic itself.

    Returns
    -------
    (InvestigationReport, json_path, html_path_or_None)
    """
    report = build_investigation_report(
        timeline_report, attribution_report, source_session_path,
    )
    json_path = write_investigation_json(report, output_dir)
    html_path = write_investigation_html(report, output_dir) if write_html else None
    return report, json_path, html_path