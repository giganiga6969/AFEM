def build_investigation_report(
    timeline_report:     TimelineReport,
    attribution_report:  AttributionReport,
    source_session_path: Optional[Path] = None,
) -> InvestigationReport:
    """
    Pure aggregation: combine Phase 3 + Phase 4 output into an
    InvestigationReport. Performs no file I/O and no analytical
    recomputation.

    Parameters
    ----------
    timeline_report :
        Phase 3 output. Its embedded ``integrity_report`` supplies all
        Phase 2 fields.
    attribution_report :
        Phase 4 output. Every attribution-related field on the returned
        InvestigationReport is copied verbatim from this object.
    source_session_path :
        Path to the original session JSONL, for provenance display.
        Relativised to the repository root when possible.

    Returns
    -------
    InvestigationReport
    """
    session_id = timeline_report.session_id
    ir = timeline_report.integrity_report  # embedded IntegrityReport, may be None defensively

    reporting_warnings: List[str] = []

    # --- Phase 2 fields (verbatim from embedded IntegrityReport) ---
    integrity_status  = getattr(ir, "verdict", "FAILED") if ir else "FAILED"
    session_complete  = getattr(ir, "session_complete", False) if ir else False
    genesis_hash      = getattr(ir, "genesis_hash", "") if ir else ""
    terminal_hash     = getattr(ir, "terminal_hash", "") if ir else ""
    verified_at       = getattr(ir, "verified_at", "") if ir else ""
    integrity_findings = list(getattr(ir, "findings", [])) if ir else []

    if ir is None:
        reporting_warnings.append(
            "TimelineReport.integrity_report was not available; integrity "
            "section fields default to their most conservative values."
        )

    # --- Consistency check (never corrects, only notes) ---
    ir_trust = getattr(ir, "evidence_trust", None) if ir else None
    ir_trust_value = getattr(ir_trust, "value", ir_trust) if ir_trust is not None else None
    if ir_trust_value is not None and ir_trust_value != attribution_report.evidence_trust:
        reporting_warnings.append(
            f"Evidence trust mismatch observed between IntegrityReport "
            f"('{ir_trust_value}') and AttributionReport "
            f"('{attribution_report.evidence_trust}'). AttributionReport's "
            f"value is preserved as authoritative; both are reported here "
            f"for investigator awareness."
        )

    # --- Phase 3 fields (verbatim from TimelineReport) ---
    user_prompt             = timeline_report.user_prompt
    timeline_entries        = list(timeline_report.entries)
    tool_sequence            = list(timeline_report.tool_sequence)
    reconstruction_anomalies = list(timeline_report.anomalies)

    # --- Phase 4 fields (verbatim from AttributionReport - never recomputed) ---
    attribution_class       = attribution_report.attribution_class
    confidence_score          = attribution_report.confidence_score
    confidence_label          = attribution_report.confidence_label
    reliable                  = attribution_report.is_reliable
    task_outcome               = attribution_report.task_outcome
    evidence_trust             = attribution_report.evidence_trust
    reconstruction_completeness = attribution_report.reconstruction_completeness
    authorization               = attribution_report.authorization
    observed_actions            = list(attribution_report.observed_actions)
    unauthorized_actions        = list(attribution_report.unauthorized_actions)
    injection_assessment        = attribution_report.injection_assessment
    injection_indicators        = list(attribution_report.injection_indicators)
    triggered_rules              = list(attribution_report.triggered_rules)
    confidence_contributions    = list(attribution_report.confidence_contributions)
    uncertainty_reasons          = list(attribution_report.uncertainty_reasons)
    supporting_evidence          = list(attribution_report.supporting_evidence)
    contradictory_evidence       = list(attribution_report.contradictory_evidence)

    # --- Phase 5 deterministic content ---
    report_id  = f"AFEM-INV-{session_id}"
    case_title = f"AFEM Investigation - {session_id}"
    case_summary = _build_case_summary(
        user_prompt, observed_actions, integrity_status, attribution_class, confidence_score,
    )
    investigator_findings = _build_investigator_findings(
        integrity_status, evidence_trust, unauthorized_actions,
        injection_assessment, injection_indicators,
    )
    investigator_conclusion = _build_conclusion(
        attribution_class, evidence_trust, injection_assessment,
    )
    limitations = _build_limitations(
        evidence_trust, reconstruction_completeness, reconstruction_anomalies,
        integrity_findings,
    )

    relativised_path = _relativise_path(source_session_path)

    return InvestigationReport(
        report_version=InvestigationReport.model_fields["report_version"].default,
        report_id=report_id,
        session_id=session_id,
        generated_at=datetime.now(timezone.utc).isoformat(),
        case_title=case_title,
        case_summary=case_summary,
        integrity_status=integrity_status,
        evidence_trust=evidence_trust,
        session_complete=session_complete,
        genesis_hash=genesis_hash,
        terminal_hash=terminal_hash,
        verified_at=verified_at,
        integrity_findings=integrity_findings,
        reconstruction_completeness=reconstruction_completeness,
        user_prompt=user_prompt,
        timeline=timeline_entries,
        tool_sequence=tool_sequence,
        reconstruction_anomalies=reconstruction_anomalies,
        attribution_class=attribution_class,
        confidence_score=confidence_score,
        confidence_label=confidence_label,
        reliable=reliable,
        task_outcome=task_outcome,
        authorization=authorization,
        observed_actions=observed_actions,
        unauthorized_actions=unauthorized_actions,
        injection_assessment=injection_assessment,
        injection_indicators=injection_indicators,
        triggered_rules=triggered_rules,
        confidence_contributions=confidence_contributions,
        uncertainty_reasons=uncertainty_reasons,
        supporting_evidence=supporting_evidence,
        contradictory_evidence=contradictory_evidence,
        investigator_findings=investigator_findings,
        investigator_conclusion=investigator_conclusion,
        limitations=limitations,
        reporting_warnings=reporting_warnings,
        source_session_path=relativised_path,
    )


